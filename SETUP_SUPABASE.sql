-- 9. Enable Realtime for these tables (IMPORTANT!)
-- Go to Database -> Replication in Supabase Dashboard and enable replication for:
-- - chat_messages
-- - code_sessions  
-- - profiles
-- - rooms
-- - room_participants
-- - users

-- 10. Set Replica Identity (needed for realtime) - CRITICAL FOR REAL-TIME UPDATES
ALTER TABLE public.chat_messages REPLICA IDENTITY FULL;
ALTER TABLE public.code_sessions REPLICA IDENTITY FULL;
ALTER TABLE public.profiles REPLICA IDENTITY FULL;
ALTER TABLE public.rooms REPLICA IDENTITY FULL;
ALTER TABLE public.room_participants REPLICA IDENTITY FULL;
ALTER TABLE public.users REPLICA IDENTITY FULL;

-- Create indexes for faster queries
CREATE INDEX IF NOT EXISTS idx_chat_messages_room_code ON public.chat_messages(room_code);
CREATE INDEX IF NOT EXISTS idx_room_participants_room_code ON public.room_participants(room_code);
CREATE INDEX IF NOT EXISTS idx_rooms_room_code ON public.rooms(room_code);

