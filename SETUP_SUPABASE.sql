DROP POLICY IF EXISTS "Allow anonymous select on profiles" ON public.profiles;
CREATE POLICY "Allow anonymous select on profiles" ON public.profiles
    FOR SELECT USING (true);

-- Rooms policies
DROP POLICY IF EXISTS "Allow anonymous insert on rooms" ON public.rooms;
CREATE POLICY "Allow anonymous insert on rooms" ON public.rooms
    FOR INSERT WITH CHECK (true);

DROP POLICY IF EXISTS "Allow anonymous update on rooms" ON public.rooms;
CREATE POLICY "Allow anonymous update on rooms" ON public.rooms
    FOR UPDATE USING (true);

DROP POLICY IF EXISTS "Allow anonymous select on rooms" ON public.rooms;
CREATE POLICY "Allow anonymous select on rooms" ON public.rooms
    FOR SELECT USING (true);

-- Room participants policies
DROP POLICY IF EXISTS "Allow anonymous insert on room_participants" ON public.room_participants;
CREATE POLICY "Allow anonymous insert on room_participants" ON public.room_participants
    FOR INSERT WITH CHECK (true);

DROP POLICY IF EXISTS "Allow anonymous select on room_participants" ON public.room_participants;
CREATE POLICY "Allow anonymous select on room_participants" ON public.room_participants
    FOR SELECT USING (true);

DROP POLICY IF EXISTS "Allow anonymous delete on room_participants" ON public.room_participants;
CREATE POLICY "Allow anonymous delete on room_participants" ON public.room_participants
